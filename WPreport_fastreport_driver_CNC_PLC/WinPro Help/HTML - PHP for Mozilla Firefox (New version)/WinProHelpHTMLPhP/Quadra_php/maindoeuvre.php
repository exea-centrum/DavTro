        <p>Le fichier des postes de main d'oeuvre contient la liste de toutes
        les opérations dont le coût est lié à une durée. Ces postes de main
        d'oeuvre peuvent représenter aussi bien le temps des ouvriers que du
        temps machine. Exemples de postes de main d'oeuvre: Découpe profil,
        montage quincaillerie, poste vitrage, centre d'usinage, ...</p>

        <p>Ils peuvent être pris en compte dans les calculs de prix de vente
        et/ou de prix de revient de la fenêtre encodée dans un devis.</p>

