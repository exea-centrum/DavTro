        <p style="text-align:left;margin-left:0;margin-right:auto;">WinPro laat
        een systeem toe van vragen voor de gebruiker voor elk type kader. U kan
        de eigenschappen van elk onderdeel aanpassen met het toevoegen van de
        door de gebruiker gedefinieerde opties, naast de basis opties
        voorgesteld door het programma.</p>

        <p></p>

        <p
        style="text-align:left;margin-left:0;margin-right:auto;"><strong>Belangrijkste
        eigenschappen van het systeem met vragen zijn:</strong></p>

        <p></p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull; Een
        lijst met vragen per kader (buitenkader, vleugel, beslag, ...)</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull; Een
        variabele wordt toegewezen aan elke vraag, en zal gebruikt worden in de
        formule voor het instellen van het verbruik van het onderdeel.</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull; Drie
        soorten vragen: logisch, numerieke, selectie.</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull;
        Vragen kunnen verborgen worden op het scherm volgens de context, en in
        functie van andere vragen.</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull;
        Vragen kunnen verborgen worden voor de afdruk volgens de context, en in
        functie van andere vragen.</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull;
        Keuze uit kleuren per vraag om de tekst en achtergrond te markeren,
        afhankelijk van de context.</p>

        <p style="text-align:left;margin-left:0;margin-right:auto;">&bull; Een
        vraag kan veranderen door formules te beantwoorden op andere vragen,
        mogelijkheid om een &#x200b;&#x200b;selectie te verbergen in een lijst.
        </p>

