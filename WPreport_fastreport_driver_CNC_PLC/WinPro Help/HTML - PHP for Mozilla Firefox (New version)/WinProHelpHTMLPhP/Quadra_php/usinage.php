      <td align="left"><p> </p>

        <p>These instructions let you define milling instructions on the
        profiles of the window (module).</p>

        <p>Basically, WinPro lets you define your millings in a logical way,
        without thinking about the constraints of the milling centre. For
        example, if a milling is generated by a fitting, but must apply to the
        outer frame profiles (frame or transom), you only need to give its
        position relatively to the fitting. WinPro will adjust the position on
        the outer frame automatically, in every possible situation. You can
        also select from which origin the milling position is to be computed,
        ignoring the requirements of your milling centre.</p>

        <p>To better meet the milling requirements, WinPro has 2 milling
        instructions, each of which supporting several sets of parameters.</p>

        <p>These instructions always follow the same basic layout: you give,
        successively, the source of the milling position, its identification,
        and its destination.</p>

        <p style="background: #fffff2;"><span
        style="font-size: 5pt; color: #000000;"> </span></p>

        <p style="margin: 0px 0px 0px 24px;">The first instruction, <span
        class="f_Variables">Milling</span>, uses any of these syntax:</p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">Milling (position, origin, type, code,
        frame, side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">Milling (position, origin, type, code,
        SPECIAL, category, side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">Milling (position, origin, type, code,
        AUTO)</span></p>

        <p class="p_Interligne"><span class="f_Interligne"
        style="font-weight: bold;"> </span></p>

        <p style="margin: 0px 0px 0px 24px;">The second instruction, <span
        class="f_Variables">MultMilling</span>, uses any of these syntax:</p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origins, code,
        frame, side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origins, code,
        SPECIAL, category, side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origins, code,
        AUTO)</span></p>

        <p class="p_Interligne"><span class="f_Interligne"
        style="font-weight: bold;"> </span></p>

        <p style="margin: 0px 0px 0px 24px;">or</p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origin, code, frame,
        side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origin, code,
        SPECIAL, category, side[, leaf])</span></p>

        <p style="margin: 0px 0px 0px 48px;"><span class="f_Variables"
        style="font-weight: bold;">MultMilling (positions, origin, code,
        AUTO)</span></p>

        <p><span class="f_Variables" style="font-weight: bold;"> </span></p>

        <p>Both sets are differentiated by their first 2 parameters:</p>

        <div
        style="text-align: left; text-indent: 0px; padding: 0px 0px 0px 0px; margin: 0px 0px 0px 16px;">

        <table border="0" cellpadding="0" cellspacing="0"
