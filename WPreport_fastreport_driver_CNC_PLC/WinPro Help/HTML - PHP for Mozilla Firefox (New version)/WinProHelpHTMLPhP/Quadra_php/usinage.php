              <td>With a <span class="f_Variables">SPECIAL</span> milling,
                WinPro works similarly, except that it looks for the first
                profile with the corresponding category. Whether this profile
                belongs to the origin element or not is irrelevant. This kind
                of milling is more flexible, but requires more work, because
                you have to set up profile categories (you can also assign
                categories dynamically...).</td>
            </tr>
          </tbody>
        </table>
        </div>

