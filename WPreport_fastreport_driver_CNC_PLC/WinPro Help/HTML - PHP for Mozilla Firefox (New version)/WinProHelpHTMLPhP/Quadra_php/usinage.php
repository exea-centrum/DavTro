        <p>Ces instructions vous permettent de définir les opération d'usinage
        à appliquer sur les profils composant le châssis (module).</p>

        <p>Fondamentalement, WinPro vous permet de définir vos usinages d'un
        point de vue logique, sans tenir compte des contraintes liées à chaque
        centre d'usinage. Par exemple, si un usinage doit être déclenché à
        partir d'une quincaillerie, mais doit s'appliquer sur le dormant ou une
        traverse de dormant, il vous suffit de spécifier la position de
        l'usinage par rapport à la quincaillerie; WinPro se chargera de
        reporter la position de l'usinage sur le dormant, en tenant compte des
        positions relatives du dormant et de la quincaillerie. De même, vous
        pouvez indiquer la position de l'usinage par rapport au point de
        référence qui vous convient le mieux (haut, bas, gauche, droite ou
        centre), sans tenir compte des contraintes liées à votre centre
        d'usinage.</p>

        <p>Afin de répondre aux différents besoins d'usinage, WinPro vous offre
        deux types d'instructions, dont chacune supporte plusieurs types de
        paramètres.</p>

