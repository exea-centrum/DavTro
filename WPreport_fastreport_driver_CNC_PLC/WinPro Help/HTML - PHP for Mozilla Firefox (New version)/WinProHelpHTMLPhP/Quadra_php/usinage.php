        <p> </p>

        <p>There are important differences between millings sent to a frame, a
        <span class="f_Variables">SPECIAL</span> milling or an <span
        class="f_Variables">AUTO</span> milling:</p>

        <div
        style="text-align: left; text-indent: 0px; padding: 0px 0px 0px 0px; margin: 0px 0px 0px 17px;">

        <table border="0" cellpadding="0" cellspacing="0"
        style="line-height: normal;">
          <tbody>
            <tr valign="baseline">
              <td width="17"><span
                style="font-size: 10pt; font-family: 'Arial Unicode MS', 'Lucida Sans Unicode', 'Arial'; color: #000000;">&bull;</span></td>
              <td>With a milling sent to a frame, WinPro computes first its
                position relatively to the elements in which it is computed.
                Then it looks up, in the direction indicated by the <span
                class="f_Variables">side</span> parameter, the first profile
                belonging to the destination frame whose position matches the
                <span class="f_Variables">side</span> parameter. Finally, it
                computes the position of the milling on that profile, using the
                position offset between that profile and the origin element.
                Although this kind of milling has many restrictions, it is the
                most used.  This is because it is the simplest to use, and it
                automatically computes the relative position of the source
                element and the destination profile.</td>
            </tr>
          </tbody>
        </table>
        </div>

        <div
        style="text-align: left; text-indent: 0px; padding: 0px 0px 0px 0px; margin: 0px 0px 0px 17px;">

