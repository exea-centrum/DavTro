              <td><span class="f_Variables">MultMilling</span> uses an <a
                href="variables.php">array variable</a> for the <span
                class="f_Variables">position</span> parameter, and either an
                array or a single value for the origin parameter (if you use a
                single value, it will be applied identically on every
                position). The maximum number of positions authorised in <span
                class="f_Variables">MultMilling</span> is <span
                style="font-weight: bold;">20</span>. By nature, the first 2
                parameters <span class="f_Variables">position</span> and <span
                class="f_Variables">origin</span> are array variables (or a
                simple variable for parameter origin); you can in no way use an
                arithmetic expression for those parameters. Of course, you can
                compute the values of the array any way you want.</td>
            </tr>
          </tbody>
        </table>
