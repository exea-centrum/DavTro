        <p class="p_Comment"><span class="f_TitreReleaseNote">Langage de script
        pour les versions mySQL :</span> Si vous utilisez WinPro avec une base
        de données au format mySQL, vous avez la possibilité de créer et
        maintenir vos propres tables grâce à un langage de script.</p>

        <p class="p_Comment">Pour que WinPro prenne en compte vos scripts, il
        suffira de créer un dossier appelé «  mySQL » dans le répertoire de
        base de WinPro, et d&rsquo;y coller vos différents scripts, sous forme
        de fichiers texte « nom_script.sql ». Les noms de fichiers et leur
        syntaxe obéissent aux règles suivantes :</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">Nom des
        fichiers :</span></p>

        <p class="p_Comment"><span
        style="text-decoration: underline;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;">Les fichiers <span
        style="font-style: italic;">« updatexxxx.sql »</span> sont réservés à
        la création des tables WinPro.</p>

        <p style="margin: 0px 0px 0px 47px;">Les fichiers <span
        style="font-style: italic;">« update1xxxx.sql »</span> sont réservés
        aux mises à jour spécifiques client gérées par Quadrature.</p>

        <p style="margin: 0px 0px 0px 47px;">Les fichiers <span
        style="font-style: italic;">« update2xxxx.sql »</span> à <span
        style="font-style: italic;">« update9xxxx.sql »</span> sont réservés
        aux programmeurs de Quadrature pour les mises à jour.</p>

        <p style="margin: 0px 0px 0px 47px;">Les fichiers <span
        style="font-style: italic;">« updateAxxxx.sql »</span> sont réservés
        aux clients pour leur propre usage; toutes les fonctionnalités décrites
        ci-dessous sont disponibles.</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment">Le premier fichier doit être nommé <span
        style="font-style: italic;">« updateA0000.sql »</span>; les fichiers
        suivant doivent être numérotés strictement séquentiellement, en
        numérotation hexadécimale. Toute rupture dans la numérotation empêchera
        l'exécution des scripts suivant cette rupture.</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span
        style="text-decoration: underline;">Entête :</span></p>

        <p class="p_Comment"> </p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">#</span>
                                        ; Indique une ligne de commentaire</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">NIL</span>                       ; Indique qu'il ne
        faut pas traiter le script</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">winpro=1
               </span>        ; Indique que le script s'applique à la version
        de production de WinPro</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">winprod=1        </span>        ; Indique que le
        script s'applique à la version devis de WinPro</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">create=1
               </span>        ; Indique que c&rsquo;est un script de création
        de table</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">update=1
               </span>        ; Indique que c&rsquo;est un script de
        modification de table</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">dict=1
        </span>                    ; Indique qu'il s'agît de modifier le
        contenu de la table <span style="font-style: italic;">dict</span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">table=xxx         </span>        ; Nom de la table
        à modifier ou à créer</p>

        <p style="margin: 0px 0px 0px 47px;"> </p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">pierret=1</span>                  ; Script
        spécifique Pierret</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">fima=1</span>                     ; Script
        spécifique Fima</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">profel=1</span>                   ; Script
        spécifique Profel</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">hemmar=1</span>                   ; Script
        spécifique Hemmar</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">tivolux=1</span>                  ; Script
        spécifique Tivolux</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment">Une et une seule des instructions '<span
        class="f_Variables">create=1</span>', <span
        style="color: #0000ff;">'</span><span
        class="f_Variables">update=1</span>' et <span
        style="color: #0000ff;">'</span><span
        class="f_Variables">dict=1</span>' DOIT être définie par script.</p>

        <p class="p_Comment">L'instruction &lsquo;<span
        class="f_Variables">NIL</span>&rsquo;, si elle se trouve en début de
        ligne, arrête l'exécution du script (WinPro passe au script suivant)</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">Corps du
        texte pour les scripts '</span><span
        class="f_Variables">create</span><span
        style="text-decoration: underline;">' et '</span><span
        class="f_Variables">update</span><span
        style="text-decoration: underline;">' :</span></p>

        <p class="p_Comment"><span
        style="text-decoration: underline;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">field=nnn;ttt[;ddd[;xxx]]</span>        ; pour
        ajouter ou modifier un champ.</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">nnn</span> est le nom du champ (en minuscules;
        WinPro le forcera de toute façon).</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">ttt</span> est le type et la longueur du champ
        (syntaxe SQL). Aucun espace ne doit être inséré ! (Exemples:
        varchar(16)).</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">ddd</span> est la valeur par défaut du champ;
        laissez un espace s'il ne faut pas de valeur par défaut mais que
        d'autres paramètres suivent sur la ligne. Valeur optionnelle, si elle
        n&rsquo;est pas définie le champ suivant <span
        class="f_Variables">xxx</span> ne pourra être défini.</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">xxx</span><span
        style="color: #0000ff;"></span> permet d'ajouter des instructions
        complémentaires, telles que <span style="color: #0000ff;">'</span><span
        class="f_Variables">not null</span><span
        style="color: #0000ff;">'</span>, <span
        style="color: #0000ff;">'</span><span class="f_Variables">primary
        key</span>', etc... Valeur optionnelle.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">-field=xxx</span>                                 ;
        pour supprimer un champ. Valable seulement en mode '<span
        class="f_Variables">update</span>'.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">index=xxx</span>                                  ;
        pour ajouter un index.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">-index=xxx</span>                                 ;
        pour supprimer un index. Valable seulement en mode '<span
        class="f_Variables">update</span>'.</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">primary
        key=xxx[;yyy[;…]]</span>        ; pour définir une clé primaire.</p>

        <div
        style="text-align: left; text-indent: 0px; padding: 0px 0px 0px 0px; margin: 0px 0px 0px 47px;">

        <table border="0" cellpadding="0" cellspacing="0"
        style="line-height: normal;">
          <tbody>
            <tr valign="baseline">
              <td width="142"><span class="f_Variables">&amp;recno</span>
                                     </td>
              <td>                       ; Pour définir un champ auto
                incrémenté, nommé '<span class="f_Variables">recno</span>'.
                Cette instruction crée automatiquement l'index nécessaire.</td>
            </tr>
