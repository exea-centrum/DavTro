        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">Corps du
        texte pour les scripts '</span><span
        class="f_Variables">dict</span><span
        style="text-decoration: underline;">' :</span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #0000ff;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">add=nnn;ttt[;ccc]</span>                          ;
        pour ajouter ou modifier un enregistrement dans la table <span
        style="font-style: italic;">dict</span>.</p>

        <p style="margin: 0px 0px 0px 47px;">                                  
                       ; <span class="f_Variables">nnn</span> est le nom de la
        table dans WinPro.</p>

        <p style="margin: 0px 0px 0px 47px;">                                 
                     ; <span class="f_Variables">ttt</span> est le nom de la
        table dans MySQL.</p>

        <p style="margin: 0px 0px 0px 47px;">                                 
                     ; <span class="f_Variables">ccc</span> est la clef
        d'importation (optionnelle).</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">sub=nnn</span>                                    
               ; Pour supprimer un enregistrement dans la table <span
        style="font-style: italic;">dict.</span></p>

        <p style="margin: 0px 0px 0px 47px;">                                  
                       ; <span class="f_Variables">nnn</span> est le nom de la
        table dans WinPro.</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">Exemple
        de script </span><span
        style="text-decoration: underline;">&lsquo;</span><span
        class="f_Variables">dict</span><span
        style="text-decoration: underline;">&rsquo;</span><span
        style="text-decoration: underline;"> de nom </span><span
        style="font-style: italic; text-decoration: underline;">« updateA0000.sql » </span><span
        style="text-decoration: underline;">:</span></p>

        <p class="p_Comment"><span
        style="text-decoration: underline;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;"><span style="color: #000080;">#
        -------------- Header ---------------------</span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">winpro=1</span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">winprod=1</span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
