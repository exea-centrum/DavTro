        <p class="p_Comment"><span class="f_TitreReleaseNote">Script language for mySQL versions:</span> If you use WinPro with a mySQL database format, you may create and
		maintain your own tables thanks to user defined scripts.</p>

        <p class="p_Comment">For the program to handle such scripts, you just need to create a directory labelled «  mySQL » into the base directory of WinPro, and paste here
		your scripts text format file with named «  script_name.sql ». File names and internal syntax to use obey the following rules:</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">File naming :</span></p>

        <p class="p_Comment"><span
        style="text-decoration: underline;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;">Files <span
        style="font-style: italic;">« updatexxxx.sql »</span> are used to create standard Winpro tables.</p>

        <p style="margin: 0px 0px 0px 47px;">Files <span
        style="font-style: italic;">« update1xxxx.sql »</span> are used for customer specific updates, as defined by Quadrature.</p>

        <p style="margin: 0px 0px 0px 47px;">Files <span
        style="font-style: italic;">« update2xxxx.sql »</span> to <span
        style="font-style: italic;">« update9xxxx.sql »</span> are reserved for standard updates, as defined by Quadrature.</p>

        <p style="margin: 0px 0px 0px 47px;">Files <span
        style="font-style: italic;">« updateAxxxx.sql »</span> may be used by customers to create their own fields and tables.</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment">The first file must be named <span
        style="font-style: italic;">« updateA0000.sql »</span>; following files must be named by strictly incrementing by 1 the number part of the name, in hexadecimal
		notation. Any break in the numbering sequence will prevent the execution of scripts after the break.</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span
        style="text-decoration: underline;">Header :</span></p>

        <p class="p_Comment"> </p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">#</span>
                                        ; starts a comment line</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">NIL</span>                       ; aborts processing of this script</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">winpro=1
               </span>        ; this script applies to the main version of WinPro</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">winprod=1        </span>        ; this script applies to the WinPro/D dealer version </p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">create=1
               </span>        ; if the table needs to be created</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">update=1
               </span>        ; if the table needs to be altered</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">dict=1
        </span>                    ; alter the contents of the dict <span style="font-style: italic;">dict</span> table.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">table=xxx         </span>        ; name of the table to be altered or created</p>

        <p style="margin: 0px 0px 0px 47px;"> </p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">pierret=1</span>                  ; if update applies only to Pierret</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">fima=1</span>                     ; if update applies only to Fima</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">profel=1</span>                   ; if update applies only to Profel</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">hemmar=1</span>                   ; if update applies only to Hemmar</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        style="color: #000080;">tivolux=1</span>                  ; if update applies only to Tivolux</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment">One and only one of the '<span
        class="f_Variables">create=1</span>', <span
        style="color: #0000ff;">'</span><span
        class="f_Variables">update=1</span>' and <span
        style="color: #0000ff;">'</span><span
        class="f_Variables">dict=1</span>' instructions MUST be defined into a single script.</p>

        <p class="p_Comment">L'instruction &lsquo;<span
        class="f_Variables">NIL</span>&rsquo;instruction, if encountered at the start of a line, aborts the current script (next script is handled by WinPro).</p>

        <p class="p_Comment"> </p>

        <p class="p_Comment"><span style="text-decoration: underline;">Body for scripts '</span><span
        class="f_Variables">create</span><span
        style="text-decoration: underline;">' and '</span><span
        class="f_Variables">update</span><span
        style="text-decoration: underline;">' :</span></p>

        <p class="p_Comment"><span
        style="text-decoration: underline;"> </span></p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">field=nnn;ttt[;ddd[;xxx]]</span>        ; to add or alter a field.</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">nnn</span> is the field name (in lower case; will be forced anyway).</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">ttt</span> is the field type and length. Do NOT insert any space! Example: varchar(16).</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">ddd</span> is the default value; leave one space if no default is to be defined but you need the extra information <span
        class="f_Variables">xxx</span>.</p>

        <p style="margin: 0px 0px 0px 189px;">                       ; <span
        class="f_Variables">xxx</span><span
        style="color: #0000ff;"></span> is some extra information, such as <span style="color: #0000ff;">'</span><span
        class="f_Variables">not null</span><span
        style="color: #0000ff;">'</span>, <span
        style="color: #0000ff;">'</span><span class="f_Variables">primary
        key</span>', etc... (optional).</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">-field=xxx</span>                                 ;
        to drop a field - available only in '<span
        class="f_Variables">update</span>' mode.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">index=xxx</span>                                  ;
        to add an index.</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">-index=xxx</span>                                 ;
        to drop an index. Available only in '<span
        class="f_Variables">update</span>' mode.</p>

        <p style="margin: 0px 0px 0px 47px;"><span class="f_Variables">primary
        key=xxx[;yyy[;…]]</span>        ; to define a primary key.</p>

