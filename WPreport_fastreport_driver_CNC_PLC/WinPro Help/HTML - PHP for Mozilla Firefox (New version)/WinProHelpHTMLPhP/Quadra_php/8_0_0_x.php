        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">add=nnn;ttt[;ccc]</span>                          ;
        to add or alter a record into <span
        style="font-style: italic;">dict</span> table.</p>

        <p style="margin: 0px 0px 0px 47px;">                                  
                       ; <span class="f_Variables">nnn</span> is the WinPro name.</p>

        <p style="margin: 0px 0px 0px 47px;">                                 
                     ; <span class="f_Variables">ttt</span> is the table name into MySQL.</p>

        <p style="margin: 0px 0px 0px 47px;">                                 
                     ; <span class="f_Variables">ccc</span> ccc is the import key (optional).</p>

        <p style="margin: 0px 0px 0px 47px;"><span
        class="f_Variables">sub=nnn</span>                                    
               ; to drop a record from <span
        style="font-style: italic;">dict</span> dict table.</p>

        <p style="margin: 0px 0px 0px 47px;">                                  
                       ; <span class="f_Variables">nnn</span> is the WinPro name.</p>

