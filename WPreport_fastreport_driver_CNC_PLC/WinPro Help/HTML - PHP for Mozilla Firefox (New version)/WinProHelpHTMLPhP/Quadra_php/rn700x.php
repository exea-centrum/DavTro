        <p>Les traductions sont stockées dans des fichiers textes (1 fichier
        par langue), stockés dans le répertoire de WinPro. Le nom de ces
        fichiers est Strings.xxx, où xxx est remplacé par le code de la langue
        correspondante. </p>

        <p>Une section <span
        style="font-weight: bold; color: #666699;">Language</span> dans
        WinPro.ini énumère chaque langue, et des informations supplémentaires
        indiquant si les fichiers de texte seront chargés ou non mémoire.</p>

        <p>Les textes des langues de base fournies avec WinPro ne sont pas
        modifiables directement. En effet, nous fournirons avec chaque nouvelle
        version de WinPro des fichiers texte modifiés, qui remplaceront ceux de
        la version précédente. Toute modification que vous feriez à ces textes
        serait alors perdue. Donc, si vous souhaitez modifier tout ou partie
        des textes d&rsquo;une langue, vous devez d&rsquo;abord créer une
        nouvelle langue, ayant la langue à modifier comme langue de
        substitution ; cette nouvelle langue sera stockée dans un fichier
        distinct de celui de la langue de base, et les modifications que vous y
        ferez seront donc préservées lors des changements de version. Les
        mécanismes de substitution vous permettent de ne modifier que les
        textes qui vous intéresse, sans toucher aux autres textes ; ceci
        réduira considérablement votre travail pour des traductions
        partielles.</p>

