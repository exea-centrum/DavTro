        <p><span class="f_TitreReleaseNote">Traductions de la base de
        données:</span> L&rsquo;interface de traduction de la base de données a
        été remaniée. Toutes ces traductions sont maintenant accessibles
        directement depuis un seul écran centralisé, où elles sont regroupées
        par type (article, dormant, &hellip;). </p>

        <p>Cela rend plus simple et rapide les traductions en série. Les
        traductions des questions éventuelles sont également accessibles depuis
        ce même écran.</p>

        <p>Le mode de stockage des traductions est également modifié : les
        traductions sont maintenant stockées dans des fichiers texte (format
        ASCII), qui sont chargés en mémoire dès le démarrage du programme. </p>

        <p>Les anciennes tables de traduction (traduct.dbf, tradques.dbf et
        tradselq.dbf) sont donc obsolètes. Elles sont maintenues en
        l&rsquo;état, mais ne seront plus mises à jour par WinPro.</p>

        <p>Ce nouveau mode de stockage offre de bien meilleures performances
        que l&rsquo;ancien, puisque tout est en mémoire dès le démarrage. De
        plus, cela permet de travailler sur les traductions sans contrainte et
        sans blocage. Par contre, si les traductions sont mises à jour depuis
        une station de travail, les autres stations de travail devront relancer
        WinPro pour en bénéficier.</p>

        <p>L&rsquo;accès à l&rsquo;écran de traduction n&rsquo;est autorisé que
        pour les utilisateurs ayant le droit &lsquo;Traductions&rsquo;, défini
        dans la fiche utilisateur.</p>

