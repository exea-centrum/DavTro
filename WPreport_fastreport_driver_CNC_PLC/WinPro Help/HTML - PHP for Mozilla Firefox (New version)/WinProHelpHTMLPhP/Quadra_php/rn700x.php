        <p> </p>

        <p>De vertalingen worden opgeslaan in de directory van Winpro in een
        tekstbestand (1 bestand/taal). De benaming is Strings.xxx, of xxx wordt
        vervangen door de corresponderende taalcode.De sectie Language in de
        WinPro.ini bekijkt alle talen, en de bijkomende informatie welke
        aangeeft of de taal in het geheugen moet opgeslagen worden of niet.</p>

        <p>De standaard teksten van de geleverde talen kunnen niet direct
        aangepast worden.Bij elke nieuwe update zullen wij de aangepaste
        standaard teksten meeleveren, deze zullen dan de reeds bestaande
        vervangen.Ele wijziging die u aldus hebt aangebracht zullen verloren
        gaan.Dus indien u delen van teksten wenst aan te passen, dient u een
        nieuwe taal aan te maken, gebruik makend van een substituuttaal;Deze
        nieuwe taal zal aldus opgeslagen worden, en de wijzigingen welke u
        aangebracht heeft zullen niet verloren gaan.Het mechanisme van de
        substituut laat u toe enkel de voor u belangrijke teksten aan te
        passen,zonder de andere teksten te wijzigen.Dit brengt de tijd voor het
        vertalen tot niets</p>

