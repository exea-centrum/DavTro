        <p><span class="f_TitreReleaseNote">Langue d&rsquo;affichage:</span> Le
        système de traduction, présent dans les versions précédentes de WinPro
        a été revu et étendu. Il s&rsquo;applique maintenant à tous les menus,
        fenêtres, documents, et d&rsquo;une manière générale à tous les textes
        internes de WinPro.</p>

        <p>WinPro vous est fourni en standard avec plusieurs langues :
        français, anglais, néerlandais, espagnol, allemand. D&rsquo;autres
        langues sont en cours de préparation.</p>

        <p>Vous disposez d&rsquo;un utilitaire externe à WinPro, nommé
        xStrings.exe, qui vous permet de définir vos propres langues, et de
        traduire les termes qui lui correspondent. L&rsquo;utilitaire de
        traduction ne vous permet pas de modifier directement les langues
        fournies avec WinPro. Pour créer vos propres traductions, vous devez
        créer une nouvelle langue, et définir les traductions qui lui
        correspondent. Cependant, afin de vous éviter de tout retraduire (il y
        a plus de 4.000 termes !), vous pouvez, au moment de la création de la
        langue, indiquer qu&rsquo;elle se substitue à une langue déjà définie.
        Les termes que vous ne traduirez pas dans la langue que vous créez
        seront recherchés dans la langue de substitution. Notez bien que cette
        notion de langue de substitution est récursive, c'est-à-dire que, si le
        terme n&rsquo;existe pas dans la langue de substitution correspondant à
        la langue que vous avez créée, WinPro le recherchera dans la langue de
        substitution de la langue de substitution, jusqu&rsquo;à trouver un
        terme défini.</p>

        <p>Comme vous n&rsquo;aurez probablement pas besoin de l&rsquo;ensemble
        des langues fournies avec WinPro, vous disposez, dans le dialogue
        Utilitaires | Préférences, d&rsquo;une page Langues, qui vous permet de
        définir quelles langues seront chargées au démarrage, et quelles
        langues seront visibles pour l&rsquo;utilisateur, lorsqu&rsquo;il
        choisit une langue.</p>

        <p>Cette nouvelle notion de langue remplace l&rsquo;ancienne table des
        langues, ainsi que la table des traductions des termes de base. Au
        premier démarrage de cette nouvelle version, WinPro récupérera ces
        anciennes langues et traductions pour les transférer dans le nouveau
        système, et vous demandera, pour chaque langue, si elle est à conserver
        ou non.</p>

        <p>Notez également que les fichiers de traduction devront être
        distribués avec WinPro/D, ainsi que le fichier Lang.ini (les fichiers
        de traduction se trouvent dans le répertoire principal de WinPro, et
        sont nommés « strings.* » - le signe * étant remplacé par les divers
        codes langue ; il y a donc un fichier par langue).</p>

        <p>Les textes sont organisés dans l&rsquo;utilitaire de traduction en
        fonction de leur utilisation. </p>

        <p>Les types de texte proposés sont :</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">CM_</span> : textes des menus</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">DOC_</span> : textes des documents client et
        fournisseurs</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">IDS_</span> : textes des écrans et documents
        internes</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">IDX_</span> : textes de l&rsquo;utilitaire
        de réindexation</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">MENU_</span> : textes des titres de menu</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">TBL_</span> : descriptions des tables de la
        base de données</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">VAR_</span> : noms des variables WinPro</p>

        <p style="margin: 0px 0px 0px 24px;"><span
        style="font-weight: bold;">XLS_</span> : textes de l&rsquo;utilitaire
        de traduction</p>

        <p> </p>

